package com.example.tuf.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Defines the spacing scale used throughout the Finance Manager app.
 * Use these values instead of hardcoded dp values to maintain consistent spacing.
 */
object Spacing {
    /** 4dp — Minimum spacing for icon padding, badge insets */
    val xs = 4.dp

    /** 8dp — Small spacing between related elements */
    val sm = 8.dp

    /** 16dp — Standard content padding, card inner spacing */
    val md = 16.dp

    /** 24dp — Section spacing, between major UI blocks */
    val lg = 24.dp

    /** 32dp — Large spacing for screen-level padding */
    val xl = 32.dp

    /** 48dp — Extra-large spacing for hero sections */
    val xxl = 48.dp
}
